const { React } = require('powercord/webpack');

module.exports = class CheckboxItem extends React.Component {
  constructor (props) {
    super(props);

    this.state = {
      checked: this.props.item.defaultState
    };
  }

  onClick () {
    const checked = !this.state.checked;

    this.props.item.onToggle(checked);
    this.setState({ checked });
  }

  render () {
    return (
      <div
        class='item-1Yvehc itemToggle-S7XGOQ'
        role='button'
        onClick={() => this.onClick()}
      >
        <div class='label-JWQiNe'>{this.props.item.name}</div>

        <div class='checkbox' role='button'>
          <div class='checkbox-inner'>
            <input type='checkbox' checked={this.state.checked} />
            <span />
          </div>
        </div>
      </div>
    )
  }
}