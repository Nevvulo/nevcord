const { React, getModuleByDisplayName } = require('powercord/webpack');

module.exports = class SliderItem extends React.PureComponent {
  constructor (props) {
    super();

    this.state = {
      percentage: props.defaultValue
    };
  }

  render () {
    const Slider = getModuleByDisplayName('slider');

    return (
      <div className='item-1Yvehc itemSlider-FZeYw0'>
        <div className='label-JWQiNe'>{this.props.name}</div>

        <Slider
          mini={true}
          handleSize={16}
          className='slider-3BOep7'
          fillStyles={this.props.color ? { backgroundColor: this.props.color } : {}}
          defaultValue={this.props.defaultValue}
          minValue={0}
          maxValue={100}
          disabled={false}
          stickToMarkers={false}
          onValueChange={this.props.onValueChange}
        />
      </div>
    );
  }
}